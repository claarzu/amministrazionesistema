package SitoAmm.Classi;

/**
 *
 * @author claar
 */
public class Prodotti {
    private int id;
    private String tipo;
    private float prezzo;
    
    public int getId()
    {
        return id;
    }    
    public void setId(int id)
    {
        this.id = id;
    }
    
    
    
    public String getTipo()
    {
        return tipo;
    }    
    public void setTipo(String tipo)
    {
        this.tipo = tipo;
    }
    
    
    
    public float getPrezzo()
    {
        return prezzo;
    }    
    public void setPrezzo(float prezzo)
    {
        this.prezzo = prezzo;
    }
    
    
}
